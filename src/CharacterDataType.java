public class CharacterDataType {
  public static void main(String[] args) {
    char e = 'E';
    char k = 'K';
    char o = 'O';

    System.out.println(e); // E
    System.out.println(k); // K
    System.out.println(o); // O

    System.out.print(e);
    System.out.print(k);
    System.out.print(o);
  }
}
